package lv01;

public class Ex01 {
	
	// 프로젝트가 실행되면 가장 첫 번째로 호출되는 메소드 : main
	public static void main(String[] args) {
		
		// 주석 : 실행되지 않는 문장
		// 실행할 문장을 여기에 작성
		// scope 지역 : {} 중괄호의 시작과 끝을 의미
		// 출력문 (암기)
		// 자동완성 키 :sysout -> ctrl +space
		
		System.out.print("Hello world");
		// ; : 세미콜론은 실행문의 마침표 역할
		
		System.out.print(10);
		System.out.println(11);
		System.out.println(12);
		System.out.print(13);
		
		// run 실행 mac : cmd + shift + fn + F11
		
	}

}
